  <script type="text/babel">
    const { useState, useEffect, useRef, useCallback } = React;

    /* ── Toast helper ─────────────────────────────────────────────────────── */
    function showToast(msg, ms = 2200) {
      const el = document.getElementById('vc-toast');
      el.textContent = msg;
      el.classList.add('show');
      clearTimeout(el._t);
      el._t = setTimeout(() => el.classList.remove('show'), ms);
    }

    /* ── Animated score counter ───────────────────────────────────────────── */
    function useCountUp(target, duration = 1000) {
      const [display, setDisplay] = useState(0);
      const frame = useRef(null);

      useEffect(() => {
        if (target === null) { setDisplay(0); return; }
        const start = performance.now();
        const step  = (now) => {
          const elapsed  = now - start;
          const progress = Math.min(elapsed / duration, 1);
          const eased    = 1 - Math.pow(1 - progress, 3);
          setDisplay(Math.round(target * eased));
          if (progress < 1) frame.current = requestAnimationFrame(step);
        };
        frame.current = requestAnimationFrame(step);
        return () => cancelAnimationFrame(frame.current);
      }, [target]);

      return display;
    }

    /* ── Progress bar ─────────────────────────────────────────────────────── */
    function ProgressBar({ score, cls }) {
      const [width, setWidth] = useState(0);
      useEffect(() => {
        const t = setTimeout(() => setWidth(score), 80);
        return () => clearTimeout(t);
      }, [score]);

      return (
        <>
          <div className="progress-track">
            <div className={`progress-fill ${cls}`} style={{ width: `${width}%` }} />
          </div>
          <div className="progress-ticks">
            <span className="progress-tick">0% HUMAN</span>
            <span className="progress-tick">50%</span>
            <span className="progress-tick">AI 100%</span>
          </div>
        </>
      );
    }

    /* ── Spinner ──────────────────────────────────────────────────────────── */
    function Spinner() {
      const msgs = ['FETCHING HTML...','SCANNING PHRASES...','ANALYZING PATTERNS...','COMPUTING SCORE...'];
      const [msgIdx, setMsgIdx] = useState(0);
      useEffect(() => {
        const iv = setInterval(() => setMsgIdx(i => (i + 1) % msgs.length), 1100);
        return () => clearInterval(iv);
      }, []);
      return (
        <div className="loading-block">
          <div className="scanner-line-wrap"><div className="scanner-line" /></div>
          <div className="loading-text">{msgs[msgIdx]}</div>
        </div>
      );
    }

    /* ── Share row ────────────────────────────────────────────────────────── */
    function ShareRow({ analyzedUrl, cardRef }) {
      /* Copy shareable ?url= link */
      const handleCopyLink = useCallback(() => {
        const link = window.location.origin + window.location.pathname
          + '?url=' + encodeURIComponent(analyzedUrl);
        if (navigator.clipboard?.writeText) {
          navigator.clipboard.writeText(link).then(() => showToast('✓ LINK COPIED'));
        } else {
          const ta = Object.assign(document.createElement('textarea'), {
            value: link, style: 'position:fixed;opacity:0'
          });
          document.body.appendChild(ta);
          ta.select();
          try   { document.execCommand('copy'); showToast('✓ LINK COPIED'); }
          catch { showToast('✕ COPY FAILED'); }
          ta.remove();
        }
      }, [analyzedUrl]);

      /* Save card as PNG via html2canvas */
      const handleSaveImage = useCallback(async () => {
        if (!cardRef.current) return;
        showToast('RENDERING...', 4000);
        try {
          const canvas = await html2canvas(cardRef.current, {
            backgroundColor: '#0d1411',
            scale: 2,
            useCORS: true,
            logging: false,
            ignoreElements: el => el.classList?.contains('share-row'),
          });
          const a = document.createElement('a');
          a.download = 'vibecheck_' + analyzedUrl.replace(/[^a-z0-9]/gi, '_').slice(0, 40) + '.png';
          a.href = canvas.toDataURL('image/png');
          a.click();
          showToast('✓ IMAGE SAVED');
        } catch { showToast('✕ RENDER FAILED'); }
      }, [cardRef]);

      /* New scan — wipe result and clean URL */
      const handleNewScan = useCallback(() => {
        history.pushState({}, '', window.location.pathname);
        window.__VC_FORCE_RESET__?.();
      }, []);

      return (
        <div className="share-row">
          <button className="share-btn" onClick={handleCopyLink} title="Copy shareable link">
            <svg viewBox="0 0 24 24"><path d="M10 13a5 5 0 0 0 7.54.54l3-3a5 5 0 0 0-7.07-7.07l-1.72 1.71"/><path d="M14 11a5 5 0 0 0-7.54-.54l-3 3a5 5 0 0 0 7.07 7.07l1.71-1.71"/></svg>
            Copy Link
          </button>
          <button className="share-btn" onClick={handleSaveImage} title="Save result as image">
            <svg viewBox="0 0 24 24"><path d="M21 15v4a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2v-4"/><polyline points="7 10 12 15 17 10"/><line x1="12" y1="15" x2="12" y2="3"/></svg>
            Save Image
          </button>
          <button className="share-btn" onClick={handleNewScan} title="Check another URL">
            <svg viewBox="0 0 24 24"><polyline points="23 4 23 10 17 10"/><path d="M20.49 15a9 9 0 1 1-2.12-9.36L23 10"/></svg>
            New Scan
          </button>
        </div>
      );
    }

    /* ── Results ──────────────────────────────────────────────────────────── */
    function Results({ data, analyzedUrl, cardRef }) {
      const { score, verdict, reasons } = data;
      const cls       = verdict === 'Likely AI' ? 'ai' : 'human';
      const displayed = useCountUp(score, 900);

      return (
        <div className="results">
          <div className="divider" />
          <div className="score-row">
            <div className={`score-number ${cls}`}>
              {displayed}<span style={{ fontSize: '40%', opacity: 0.6 }}>%</span>
            </div>
            <div className={`verdict-badge ${cls}`}>{verdict}</div>
          </div>

          <ProgressBar score={score} cls={cls} />

          <div className="reasons-label">▸ ANALYSIS OUTPUT</div>
          {reasons.map((r, i) => (
            <div className="reason-item" key={i} style={{ animationDelay: `${i * 0.12}s` }}>
              <span className="reason-bullet">◆</span>
              <span>{r}</span>
            </div>
          ))}

          <ShareRow analyzedUrl={analyzedUrl} cardRef={cardRef} />
        </div>
      );
    }

    /* ── Main App ─────────────────────────────────────────────────────────── */
    function App() {
      const [url,      setUrl]      = useState('');
      const [loading,  setLoading]  = useState(false);
      const [result,   setResult]   = useState(null);
      const [error,    setError]    = useState('');
      const [analyzed, setAnalyzed] = useState('');
      const cardRef = useRef(null);

      /* Expose reset for ShareRow's "New Scan" button */
      window.__VC_FORCE_RESET__ = () => {
        setUrl(''); setResult(null); setError(''); setAnalyzed('');
      };

      /* Auto-run from PHP-injected preload (shareable ?url= link) */
      useEffect(() => {
        if (window.__VC_PRELOAD__) {
          const { url: preUrl, result: preResult } = window.__VC_PRELOAD__;
          setUrl(preUrl);
          setAnalyzed(preUrl);
          setResult(preResult);
          window.__VC_PRELOAD__ = null;
        } else if (window.__VC_PRELOAD_ERROR__) {
          setError(window.__VC_PRELOAD_ERROR__);
          window.__VC_PRELOAD_ERROR__ = null;
        }
      }, []);

      const handleCheck = async () => {
        if (!url.trim()) return;
        setLoading(true);
        setResult(null);
        setError('');
        setAnalyzed('');

        try {
          const res  = await fetch(window.location.pathname, {
            method:  'POST',
            headers: { 'Content-Type': 'application/json' },
            body:    JSON.stringify({ url: url.trim() }),
          });
          const data = await res.json();
          if (!res.ok || data.error) throw new Error(data.error || `HTTP ${res.status}`);
          setAnalyzed(url.trim());
          setResult(data);
        } catch (e) {
          setError(e.message || 'Could not reach the server');
        } finally {
          setLoading(false);
        }
      };

      const handleKey = (e) => { if (e.key === 'Enter' && !loading) handleCheck(); };

      return (
        <>
          <div className="header">
            <div className="header-eyebrow">◈ Diagnostic System v1.0</div>
            <div className="header-title">VIBE CHECKER</div>
            <div className="header-sub">
              AI-generated or human-written? Enter a URL to find out.
            </div>
          </div>

          <div className="card" ref={cardRef}>
            <div className="card-inner">
              <label className="input-label" htmlFor="url-input">▸ TARGET URL</label>
              <div className="input-row">
                <input
                  id="url-input"
                  className="url-input"
                  type="url"
                  value={url}
                  placeholder="https://example.com"
                  onChange={(e) => setUrl(e.target.value)}
                  onKeyDown={handleKey}
                  disabled={loading}
                  autoComplete="off"
                  spellCheck="false"
                />
                <button className="btn" onClick={handleCheck} disabled={loading || !url.trim()}>
                  {loading ? '...' : 'CHECK VIBE'}
                </button>
              </div>

              {error && <div className="error-bar">{error}</div>}
              {loading && <Spinner />}
              {result && !loading && (
                <Results data={result} analyzedUrl={analyzed} cardRef={cardRef} />
              )}
            </div>
          </div>

          <div className="footer">
            VIBE CHECKER — PATTERN ANALYSIS ENGINE &nbsp;|&nbsp; NO DATA STORED
          </div>
        </>
      );
    }

    ReactDOM.createRoot(document.getElementById('root')).render(<App />);
