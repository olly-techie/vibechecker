// POST (application/json) — JSON API used by React fetch()
$contentType = $_SERVER['CONTENT_TYPE'] ?? '';
if ($_SERVER['REQUEST_METHOD'] === 'POST' && str_contains($contentType, 'application/json')) {
    header('Content-Type: application/json');
    header('Access-Control-Allow-Origin: *');

    $input  = json_decode(file_get_contents('php://input'), true);
    $rawUrl = trim($input['url'] ?? '');

    if (empty($rawUrl))                             { http_response_code(400); echo json_encode(['error' => 'No URL provided']); exit; }
    if (!preg_match('#^https?://#i', $rawUrl))      $rawUrl = 'https://' . $rawUrl;
    if (!filter_var($rawUrl, FILTER_VALIDATE_URL))  { http_response_code(400); echo json_encode(['error' => 'Invalid URL']); exit; }

    $host = parse_url($rawUrl, PHP_URL_HOST);
    $ip   = gethostbyname($host);
    if (filter_var($ip, FILTER_VALIDATE_IP, FILTER_FLAG_NO_PRIV_RANGE | FILTER_FLAG_NO_RES_RANGE) === false) {
        http_response_code(403); echo json_encode(['error' => 'Private or reserved IP addresses are not allowed']); exit;
    }

    try {
        $html   = fetchHtml($rawUrl);
        $result = analyzeHtml($html, $AI_PHRASES, $BUILDER_SIGNATURES, $TAILWIND_UTILITY_PATTERNS, $AI_COMMENT_PATTERNS, $AI_SECTION_PATTERNS);
        echo json_encode($result);
    } catch (RuntimeException $e) { http_response_code(422); echo json_encode(['error' => $e->getMessage()]); }
    catch (Throwable $e)          { http_response_code(500); echo json_encode(['error' => 'Internal server error']); }
    exit;
}

